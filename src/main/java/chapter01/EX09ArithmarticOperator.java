package chapter01;

public class EX09ArithmarticOperator {

	public static void main(String[] args) {
		System.out.println(10.0/3.0); // 결과 double
		System.out.println(10.0/3); // 결과 double
		System.out.println(10/3.0); // 결과 double
		System.out.println(10/3); // 결과 int
		
		System.out.println(44 % 3); // 결과 int
		System.out.println(-7 % 3); // 결과 -1 기준 몫
		System.out.println(7 % -3); // 결과 1

	}

}
