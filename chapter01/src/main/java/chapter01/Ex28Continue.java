package chapter01;

public class Ex28Continue {

	public static void main(String[] args) {
		forTest();
		whileTest();
	}
	static void forTest() {
		for(int i =0; i <10; i++) {
			System.out.println("!!!!");
	}
	}	
	static void whileTest() {
		int i = 0;
		while(i < 10) {
			System.out.println("!!!!");
			i++;
		}
	}
		
	}

